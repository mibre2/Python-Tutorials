# Store string within variable called "message"
# You can make string using single quotes '' or double quotes ""
message = 'Meet me today'
print(message)

message2 = "Meet me tonight"
print(message2)

# 2 different quotes in the same string 
message3 = "I'm not going to be there"
print(message3)

message4 = '"She said do you love me? I told her only partly. I only love my bed and my momma, Im sorry"'
print(message4)
